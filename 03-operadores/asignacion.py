# Operadores de Asignacion

edad = 55

print(edad)

edad += 5  # suma 5 a edad

edad = edad + 5 # suma 5 a edad

edad -= 5  # resta 5 a edad
edad *= 2  #multiplico *2 la edad 
edad /= 5  # divido entre 5 la edad

print(edad)  # imprime edad 

# aqui " -= n "  estamos haciendo una resta a un valor de la variable 

# por ejemplo " valor = valor - n "    se puede sustituir por "valor -= n"v  cumpliendo asi la resta.


