"""
 SET es un tipo de dato, para tener una coleccionde valores,
 pero no tiene ni indice ni orden.
"""

personas = {
    "victor",
    "manolo",
    "pendeje",
    "kakaroto"
}

personas.add("pacojerte") # .add  agrega un elemento al set
personas.remove("kakaroto")  # eliminamos un elemento del set

print(type(personas))  # tipo de dato set
print(personas)  # imprimimos personas.



